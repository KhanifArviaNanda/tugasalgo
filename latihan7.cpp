#include <iostream>
using namespace std;

int main() {
    
    int c;
   
    int a = 21;
    int b = 10;
    
    c = a + b;
    cout << c << endl; 
    
    c = a - b;
    cout << c << endl;
    
    c = a * b;
    cout << c << endl; 
   
    c = a / b;
    cout << c << endl; 
     
    c = a % b;
    cout << c << endl; 
  
    c = a++;
    cout << c << endl;
    
    c = a--;
    cout << c << endl;
    
    return (0);
}